const MEMORY_DIR = process.env.MEMORY_DIR || path.resolve(SCRIPT_DIR, '../../memory');
const GRAPH_OUT  = MEMORY_DIR + '/graph.json';
const HTML_OUT   = MEMORY_DIR + '/brain.html';
const EXCLUDE    = new Set(['MEMORY.md', '_template.md', 'graph.json', 'brain.html']);
const MARKER     = '<!-- generated by brain rebuild, do not edit -->';
const MIN_WEIGHT = 0.25;

